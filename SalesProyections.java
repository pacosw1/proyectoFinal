class SalesProyections{
  private double expenses;//Spending that varies(I.e. Inventory)
  private double costs;//Fixed Spending(I.e. Salary)
  private double sales;
  public SalesProyections(){

  }
  //Methods
  public double equilibriumPoint(){//Sales needed to cover expenses/costs
    double total = 0;
    //Add all expenses
    //total = Expenses added/(grossMargin/100)
    return total;
  }
  public double grossMargin(){//Later has to be converted into percentage
    return (sales - (expenses + costs))/sales;
  }
}
